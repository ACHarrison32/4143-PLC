package main

import "fmt"

func main() {
	// Integer types
	var num int = 42
	var age = 30

	// Floating-point types
	var pi float32 = 3.14
	var height = 5.8

	// String type
	var message string = "Hello, Go!"

	// Boolean type
	var isTrue bool = false

	fmt.Println("num:", num)
	fmt.Println("age:", age)
	fmt.Println("pi:", pi)
	fmt.Println("height:", height)
	fmt.Println("message:", message)
	fmt.Println("isTrue:", isTrue)
}
